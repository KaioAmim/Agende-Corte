function formatMoney(value) {
    value = value.replace(/\D/g, ""); // Remove qualquer coisa que não seja dígito
    value = (value / 100).toFixed(2) + ""; // Divide por 100 e fixa 2 casas decimais
    value = value.replace(".", ","); // Troca ponto por vírgula
    value = value.replace(/(\d)(?=(\d{3})+(?!\d))/g, "$1."); // Insere pontos a cada 3 dígitos
    return "R$ " + value;
}

document.getElementById("money").addEventListener("input", function (e) {
    this.value = formatMoney(this.value);
 });




